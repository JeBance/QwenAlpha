/**
 * Обработчик команды /instructions
 * Отправка инструкций по настройке и использованию бота
 * @param {import('telegraf').Context} ctx
 */
async function instructionsHandler(ctx) {
  const isSuperAdmin = ctx.state.isSuperAdmin;

  const instructionsText = `
📖 <b>Qwen Alpha — Инструкции по настройке</b>

${isSuperAdmin ? `👑 <b>Для супер-администратора:</b>

<b>1. Настройка системного промпта</b>

Системный промпт определяет поведение бота. Вы можете настроить его под свои задачи:

/setSystemPrompt &lt;ваш промпт&gt;

<b>Примеры:</b>

• Консультант магазина:
/setSystemPrompt Ты — консультант магазина электроники. Ассортимент: телевизоры, холодильники. Отвечай кратко, предлагай товары.

• Техподдержка:
/setSystemPrompt Ты — техподдержка CRM-системы. Тарифы: Старт (990₽), Про (1990₽). Помогай с настройкой.

• Юридический бот:
/setSystemPrompt Ты — юридический консультант. Специализация: договоры, ИП, ООО. Отвечай на основе законодательства РФ.

<b>2. Просмотр текущего промпта:</b>
/getSystemPrompt

<b>3. Сброс к промпту по умолчанию:</b>
/resetSystemPrompt

<b>4. Управление администраторами:</b>
/admin add &lt;user_id&gt; — Добавить админа
/admin remove &lt;user_id&gt; — Удалить админа
/admin ban &lt;user_id&gt; — Забанить пользователя
/admin unban &lt;user_id&gt; — Разбанить пользователя

` : ''}<b>📱 Для всех пользователей:</b>

<b>1. Начало работы:</b>
/start — Запуск бота и приветствие

<b>2. Работа с кодом:</b>
• Отправьте код текстом — бот проанализирует его
• Отправьте файл с кодом (.js, .py, .ts, etc.) — бот изучит и даст рекомендации
• Задайте вопрос по коду — бот объяснит

<b>3. Контекст диалога:</b>
• Бот помнит историю переписки в сессии
• Ответьте на сообщение бота — продолжите диалог
• /reset — Начать новую сессию

<b>4. Статистика и настройки:</b>
/stats — Ваша статистика использования
/settings — Настройки бота

<b>🔒 Безопасность:</b>

• Бот не имеет доступа к файловой системе сервера
• Бот не выполняет shell-команды
• Бот не раскрывает API-ключи и токены
• Супер-админ имеет расширенные права

<b>⚙️ Технические детали:</b>

• Qwen Code (headless режим)
• Telegram Bot API
• JSON хранилище (~/.qwen-alpha/)
• Node.js &gt;= 18.0.0

<b>📞 Поддержка:</b>

• Репозиторий: https://github.com/JeBance/QwenAlpha
• Issues: https://github.com/JeBance/QwenAlpha/issues
  `.trim();

  await ctx.reply(instructionsText, {
    parse_mode: 'HTML',
    disable_web_page_preview: true,
  });
}

module.exports = {
  instructionsHandler,
};
